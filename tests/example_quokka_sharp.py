section("2 · VERIFICATION")

# ── What to verify ────────────────────────────────────────────────────────────
#
# qasmfile : path to the circuit to verify
# basis    : "comp" or "pauli"
# precons  : dict of {qubit_index: value} — the required INPUT state
# postcons : dict of {qubit_index: value} — the required OUTPUT state
#
# Result: "True"  → the circuit always maps precons → postcons
#         "False" → it does NOT
#
# Try changing the variables below:

VER_FILE     = fixture("verify", "v04_cx_control1.qasm")  # ← change me
VER_BASIS    = "comp"                                      # ← "comp" or "pauli"
VER_PRECONS  = {0: 0, 1: 0}                               # ← input  state constraints
VER_POSTCONS = {0: 1, 1: 1}                               # ← output state constraints

# ── Run ───────────────────────────────────────────────────────────────────────

verified = qk.functionalities.verify(
    VER_FILE,
    basis    = VER_BASIS,
    precons  = VER_PRECONS,
    postcons = VER_POSTCONS,
)

result_line("Circuit:",      os.path.basename(VER_FILE))
result_line("Basis:",        VER_BASIS)
result_line("Precondition:", VER_PRECONS)
result_line("Postcondition:",VER_POSTCONS)
result_line("Result:",       verified)

