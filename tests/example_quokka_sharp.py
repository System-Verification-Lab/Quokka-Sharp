# ══════════════════════════════════════════════════════════════════════════════
# 4.  SYNTHESIS
#     Find the shortest circuit that implements a given target unitary,
#     using only gates from a chosen gate set.
#
#     NOTE: synthesis requires a *maximum* WMC solver (e.g. d4max).
#           Make sure your config.json points at d4max and sets D4ToolInvocation.
# ══════════════════════════════════════════════════════════════════════════════

section("4 · SYNTHESIS")

# ── What to synthesise ────────────────────────────────────────────────────────
#
# qasmfile : target circuit (the unitary you want to re-implement)
# gate_set : allowed gates — choose from: "h", "cx", "s", "t", "cz", "csqrtx"
#            Use either "t" or "csqrtx" to synthesise CCX, but not both.
# epsilon  : fidelity tolerance (0 = exact, 0 < epsilon < 1 = approximate)
#
# Returns (outcome, weight, qasm_string, layers):
#   outcome     : "FOUND" | "TIMEOUT" | "CRASH" | "ERROR#"
#   weight      : achieved fidelity (1.0 when FOUND with epsilon=0)
#   qasm_string : the synthesised circuit in QASM format
#   layers      : circuit depth of the best solution found

SYN_FILE     = fixture("synthesis", "syn01_h_target.qasm")  # ← change me
SYN_GATE_SET = {"h", "cx", "s"}                             # ← change me
SYN_EPSILON  = 0                                             # ← 0 for exact

# ── Run ───────────────────────────────────────────────────────────────────────

outcome, weight, qasm_str, layers = qk.functionalities.syn(
    SYN_FILE,
    epsilon  = SYN_EPSILON,
    gate_set = SYN_GATE_SET,
)

result_line("Target circuit:",  os.path.basename(SYN_FILE))
result_line("Gate set:",        str(SYN_GATE_SET))
result_line("Epsilon:",         SYN_EPSILON)
result_line("Outcome:",         outcome)
result_line("Fidelity (weight):",f"{float(weight):.6f}" if weight is not None else "—")
result_line("Depth (layers):",  layers)

if outcome == "FOUND" and qasm_str:
    print()
    print("  Synthesised circuit:")
    print("  " + "-" * 50)
    for line in qasm_str.strip().splitlines():
        print(f"    {line}")
    print("  " + "-" * 50)

