import os
import decimal
import quokka_sharp as qk

# ─── helpers ──────────────────────────────────────────────────────────────────

def to_float(v) -> float:
    """Convert Decimal / float / int to plain Python float."""
    return float(v)

FIXTURES = os.path.join(os.path.dirname(__file__), "qasm_fixtures")

def fixture(subdir: str, filename: str) -> str:
    """Return the path to a bundled fixture file."""
    return os.path.join(FIXTURES, subdir, filename)

def section(title: str):
    width = 60
    print()
    print("═" * width)
    print(f"  {title}")
    print("═" * width)

def result_line(label: str, value):
    print(f"  {label:<30} {value}")

# ══════════════════════════════════════════════════════════════════════════════
# 1.  SIMULATION
#     Compute the probability of a measurement outcome after running a circuit.
# ══════════════════════════════════════════════════════════════════════════════

section("1 · SIMULATION")

# ── What to simulate ──────────────────────────────────────────────────────────
#
# qasmfile   : path to a .qasm circuit file
# basis      : "comp"  – computational basis  (recommended for most circuits)
#              "pauli" – Pauli basis
# measurement: "allzero"   – probability that ALL qubits read 0
#              "firstzero" – probability that the first qubit reads 0
#              {qubit_index: 0_or_1, ...} – specific outcome, e.g. {0:1, 1:0}
#
# Try changing any of the three variables below:

SIM_FILE        = fixture("multi_qubit", "bell.qasm")   # ← change me
SIM_BASIS       = "comp"                                 # ← "comp" or "pauli"
SIM_MEASUREMENT = "allzero"                              # ← "allzero" / "firstzero" / {0:1, 1:0} / ...

# ── Run ───────────────────────────────────────────────────────────────────────

prob = qk.functionalities.sim(
    qasmfile    = SIM_FILE,
    basis       = SIM_BASIS,
    measurement = SIM_MEASUREMENT,
)

result_line("Circuit:",      os.path.basename(SIM_FILE))
result_line("Basis:",        SIM_BASIS)
result_line("Measurement:",  SIM_MEASUREMENT)
result_line("Probability:",  prob if prob in ("TIMEOUT", "MEMOUT") else f"{to_float(prob):.10f}")

